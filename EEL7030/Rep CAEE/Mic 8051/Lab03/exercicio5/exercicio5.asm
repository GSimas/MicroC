;MATHEUS FIGUEIREDO
CS 		  EQU P0.7
END0 	  EQU P3.3;
END1 	  EQU P3.4;
ATRASO 	  EQU 05H
		  ORG 0h
		  CLR END0
		  CLR END1
		  CLR CS	
		 
		MOV P1,#00000010B
CIC:	CALL LED
		JMP CIC



LED:	MOV A,#ATRASO
		CALL DELAY
		MOV A,P1
		RL A
		MOV P1,A
		RET
		

DELAY:	DJNZ Acc,DELAY 
	 	RET	  
	
		END
